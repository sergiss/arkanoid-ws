	public static void loadLevel(int[] level, World world) {
		
		float width  = world.getWidth();
		float height = world.getHeight();
		
		int cols = 21;
		int rows = 12;
		
		float tw = width / cols;
		float th = (height * 0.5F) / rows;
		
		float hw = width * 0.5F;
		float hh = height * 0.5f;
		
		for(int i = 0; i < cols ; i++) {
			for(int j = 0; j < rows ; j++) {
				int color = level[j * cols + i];
				if(color != 0) {
					Rectangle rectangle = new Rectangle(color == 1 ? Utils.random.nextInt(9) : (color - 2), tw, th);
					rectangle.position.set(i * tw, j * th).sub(hw - tw * 0.5F, hh - th * 0.5F);
					world.add(rectangle);
				}
				
			}
		}
		
	}

